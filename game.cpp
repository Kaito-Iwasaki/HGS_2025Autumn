//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	InitDecal();

	InitPlayer();

	InitBullet();

	InitEnemy();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitDecal();

	UninitPlayer();

	UninitBullet();

	UninitEnemy();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	UpdatePlayer();

	UpdateBullet();

	UpdateEnemy();

	if (GetEndingTimer() <= 0 || GetFade().state == FADESTATE_NONE)
	{
		SetFade(SCENE_RESULT);
	}
}

//=====================================================================
// 終了処理
//=====================================================================
void DrawGame(void)
{
	DrawDecal();

	DrawPlayer();

	DrawBullet();

	DrawEnemy();
}
