//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	InitPlayer();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitPlayer();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	UpdatePlayer();

	if (GetKeyboardTrigger(DIK_RETURN))
	{
		SetFade(SCENE_RESULT);
	}
}

//=====================================================================
// 終了処理
//=====================================================================
void DrawGame(void)
{
	DrawPlayer();
}
