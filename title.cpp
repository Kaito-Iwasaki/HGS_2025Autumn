//=====================================================================
// 初期化処理
//=====================================================================
void InitTitle(void)
{
	InitPlayer();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitTitle(void)
{
	UninitPlayer();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateTitle(void)
{
	UpdatePlayer();

	if (GetKeyboardTrigger(DIK_RETURN))
	{
		SetFade(SCENE_TITLE);
	}
}

//=====================================================================
// 終了処理
//=====================================================================
void DrawTitle(void)
{
	DrawPlayer();
}
